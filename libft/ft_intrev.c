#include "libft.h"

int		*ft_intrev(int *s, size_t n)
{
	size_t	i;
	int		tmp;

	i = 0;
	while (i < n)
	{
		n--;
		tmp = s[i];
		s[i] = s[n];
		s[n] = tmp;
		i++;
	}
	return (s);
}
